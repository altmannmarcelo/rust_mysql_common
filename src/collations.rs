#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, thiserror::Error)]
#[error("Unknown collation ID {}", _0)]
pub struct UnknownCollationID(pub u16);

impl From<UnknownCollationID> for u16 {
    fn from(x: UnknownCollationID) -> Self {
        x.0
    }
}

/// MySQL server collation
#[derive(Debug, Clone, PartialEq, Eq, Hash)]
pub struct Collation<'a> {
    pub id: CollationID,
    pub charset: &'a str,
    pub collation: &'a str,
    pub is_default: bool,
    pub padding: PadAttribute,
    pub is_compiled: bool,
    pub sort_len: u8,
    pub max_len: u8,
}

impl<'a> Collation<'a> {
    /// Get the collation ID.
    pub fn id(&self) -> CollationID {
        self.id
    }

    /// Get the charset.
    pub fn charset(&self) -> &str {
        self.charset
    }

    /// Get the collation.
    pub fn collation(&self) -> &str {
        self.collation
    }

    /// Is the collation default?
    pub fn is_default(&self) -> bool {
        self.is_default
    }

    /// Get the padding attribute.
    pub fn padding(&self) -> PadAttribute {
        self.padding
    }

    /// Is the collation compiled?
    pub fn is_compiled(&self) -> bool {
        self.is_compiled
    }

    /// Get the sort length.
    pub fn sort_len(&self) -> u8 {
        self.sort_len
    }

    /// Get the max length.
    pub fn max_len(&self) -> u8 {
        self.max_len
    }
}

